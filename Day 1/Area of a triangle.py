def area(a, b, c):  # function for finding area of a triangle
    s = (a + b + c) / 2
    v1 = s - a
    v2 = s - b
    v3 = s - c
    v4 = s * v1 * v2 * v3
    an = v4 ** 0.5
    return an


a = float(input('Enter the length of the first side= '))
b = float(input('Enter the length of the second side= '))
c = float(input('Enter the length of the third side= '))
print(area(a,b,c))
