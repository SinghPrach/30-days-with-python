# Converting decimal number to binary, octal, and hexadecimal

num = int(input("Enter any decimal number= "))
bin_num = bin(num)
oct_num = oct(num)
hex_num = hex(num)
print('Binary form of', num, 'is', bin_num)
print('Octal form of', num, 'is', oct_num)
print('Hexadecimal form of', num, 'is', hex_num)
