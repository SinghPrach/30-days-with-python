#String to int
num_string = '2000'
num_int = int(num_string)

# print the type
print(type(num_string))

# print the value
print(num_int)

#String to float
num_string = '2000.2'
num_float = float(num_string)

# print the type
print(type(num_float))

# print the value
print(num_float)

#Float to int
num_string = '2000.2'
num_int = int(float(num_string))

# print the type
print(type(num_int))

# print the value
print(num_int)
